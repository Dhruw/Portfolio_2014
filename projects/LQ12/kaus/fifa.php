<h4>RULES AND REGULATION</h4>
PES Version: 2011<br />
Playing media- Play Station 2<br />
<ol>
	<li>	The tournament will be of knock out type.</li>
    <li>	The total number of rounds will be decided by number of  participants </li>
    <li>	Selection of opponent will be random.</li>
    <li>	In case of draw final results will be declared by penalty shootout.</li>
    <li>	Extra time will be provided in quarter final, semi final and final.</li>
    <li>	2 Mins will be provided to each player for setting his formation before the kick off.</li>
    <li>	One can change his formation only thrice a game (1 during first half, 2nd in the half time, 3rd in the second half) and time allowed is 1 minute for each pause.</li>
    <li>	In case of any technical problem which cannot be solved during the match the players will have to restart the match irrespective to the score (because soccer is unpredictable).</li>
    <li>	Any misbehaving player will be disqualified immediately.</li>
    <li>	Decision of organizing committee will be final.</li>
    <li>	Game settings (like match length, number of substitutes) will be declared at the time of event.</li>
</ol>
