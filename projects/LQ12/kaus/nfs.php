 <h3>INTRODUCTION:</h3>      
<p>
Combining illicit street racing a tuner customization with the intensity of strategic police pursuit that surpasses any Hollywood-style chase scene, Need for Speed Most wanted will be on every gamers 'must-have' list this holiday season. Out-race rivals, evade cops and exploit hundreds of miles of open road as gamers make their way up to Blacklist.<br />
To become the most wanted racer, players must build up their street creed and Rap Sheet with gripping, while knuckle, head-to-head races with the top drivers on the streets, as well as pull daring, evasive moves to out-run and out-fox the cops that patrol the open road.<br />
Layers can manage their heat or utilize a number of strategic tactics to keep the cops off their tails as they leave their rivals behind to their exhaust fumes.<br />
Need for Speed Most Wanted also features a variety of the hottest cars that ranges from tuners to super cars. You can customize your ride to race against any class of vehicles. The streets will be your playground this fall.
