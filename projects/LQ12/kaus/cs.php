<h4>MATCH FORMAT:</h4>
<h4>QUALIFICATION & QUARTERS ROUND:</h4>
<ul>
	<li>	Every match will be played on one map with a total of 12 rounds.</li>
    <li>	6 round each as Counter Terrorist and Terrorist.</li>
    <li>	The start money for the match will be $800.</li>
    <li>	The team first to win 7 matches will be the winner of the match.</li>
    <li>	In case of a tie or draw match, 3 extra rounds will be played continuously, the team which wins first of the 2 rounds, will win that match.</li>
</ul>
<h4>SEMI-FINAL:</h4>
<ul>
	<li>	The number of rounds will be 16 and one map will be played (Any one map will be selected by the teams).</li>
    <li>	8+8 rounds each as Counter Terrorist and Terrorist.</li>
    <li>	The starting money will be decided on the spot, minimum $800.</li>
    <li>	The team which wins maximum rounds will be the winner of the match.</li>
    <li>	In case of a tie or draw match, 5 extra rounds will be played continuously, the team which wins first of the 3 rounds, will win that match.</li>
</ul>
<h4>FINALS:</h4>
<ul>
	<li>	The number of rounds will be 24 and two maps will be played(maps will be selected by the teams).</li>
    <li>	12+12 rounds each as Counter Terrorist and Terrorist.</li>
    <li>	The starting money will be decided on the spot, minimum $800.</li>
    <li>	In case of a tie or draw match, 10 extra rounds will be played continuously, the team which wins first of the 6 rounds, will win that match.</li>
</ul>
<h4>GENERAL RULES:</h4>
<ul>
	<li>	The tournament will be 5 Vs. 5 knock out. A team wins the stage will be advances to the next stage. The knock out continues till the 4 top scoring teams made it to the semi-finals.</li>
    <li>	Players must be present in the tournament area with the whole team at the notifications schedule. Players are advised to be present a few minutes before the schedule to avoid any unpredictable delay. If the team is not present at the notification schedule, they shall be disqualified.</li>
    <li>	A toss or knife round will be done before a match to decide the side team wants to take.</li>
    <li>	The players have the freedom to bring and use their own controlling device, that is, Mouse (USB only)& Headphone.</li>
    <li>	If players are willing to bring their own controllers, be sure it don't need any extra drivers on windows xp and also we take no responsibility for your stuffs.</li>
    <li>	All players must have their clan tags before their name, without < or ></li>
    <li>	If the match is involuntary interrupted (player crash, server crash, etc.):</li>
    <ul>
    	<li>	If one of the players is disconnected or crashed, he must come back in to the game as soon as possible and the round will be restarted.</li>
        <li>	If half rounds of the match are already over, then only next half rounds will be restarted, else the whole match will be restarted.</li>
        <li>	No compensation will be given in the money earned in game and lost due to interruption of the match.</li>
        <li>	The team should be of 5 members and no change of players in the team is allowed after they have played their first match.</li>
    </ul>
</ul>
