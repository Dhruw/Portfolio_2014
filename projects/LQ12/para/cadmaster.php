</ul>
<h4>ROUND 1: Simple 2-D drawing:</h4>
<ul>
<li>	Participant who completes given drawing in minimum time will qualify.</li>
<li>	This round is to test your accuracy and speed of drawing. </li>

</ul>
<h4>ROUND 2: 3-D drawing:		</h4>
<ul>
<li>	Participant have to prepare a 3-d model by reference of provided front ,top and side view </li>
<li>	Participant who completes given drawing in minimum time will qualify.</li>
<li>	In case of tie round two is repeated with another drawing.</li>
</ul>
<h4>ROUND 3: Assembly:</h4>
<ul>
<li>	In this round participant have to model and assemble component by reference of given drawing.</li>
<li>	The participant who will complete the drawing in minimum time will be the winner.</li>

</ul>

