<h4>RULES AND REGULATIONS:</h4>
<ul>
<li>	Maximum 2 members per team.</li>
<li>	There will be 3 rounds.</li>
<li>	The team which completes the circuit in minimum time will win.</li>
<li>	Depending upon the no. of participants, criteria of selection will be decided.</li>
</ul>
<h4>ROUND 1: IDENTIFICATION OF THE COMPONENTS & ITS VALUES:</h4>
<ul>
<li>	Herein the selected teams will try to identify the components & its values. </li>
<li>	The participants will be given a maximum of 5 Min. to identify the components and their values.</li>
<li>	Selected teams identifying the maximum components with their correct values within the time limit will move to second round.</li>
</ul>
<h4>ROUND 2: CIRCUIT SIMULATION:	</h4>
<ul>
<li>	Each team would be provided with a PC having Multisim software (circuit simulation software).</li>
<li>	The participants will try to design the specified circuit within the time limit will move to third round</li>
</ul>
<h4>ROUND 3: CIRCUIT DESIGNING IN BREAD BOARD:	</h4>
<ul>
<li>	Before connecting the power supply, the circuit has to be approved by the judge.</li>
<li>	The circuit designed by the participant must give the desired output. </li>
<li>	Teams completing the circuit first within the time limit will be the winner.</li>
</ul>
</div>

