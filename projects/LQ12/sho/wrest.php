<ul>
<li>	Arena of diameter 200 cm.</li>
<li>	It is made up of wooden base with rubber matt.</li>
<li>	Wooden base will be placed at 150mm above.</li>
</ul>
<h4>ROBOT: </h4>
<ul>
<li>	Robot can be made out of any material. Any type or size of electric motor or electric-powered locomotion can be used.</li>
<li>	Robot should be fit in the box of 300mmX250mmX250mm (L x B x H) at any point of the game.</li>
<li>	Robots may have maximum mass of 4 kilograms or less.</li>
<li>	The control of robot can be wired or wireless.</li>
<li>	The power supply should not exceed 12volts, and can be on and off board.</li>
