<h4>RULES AND REGULATIONS: </h4>
<ul>
<li>	3 participants per team.</li>
<li>	Robot cannot damage the arena at the time of match otherwise the team will be disqualify. </li>
</ul>
<h4>PLAYOFFS</h4>
<ul>
<li>	Fair chance will be given to all participants.</li>
<li>	Depending on how many robots participate, the judges can choose a number of different elimination methodologies. Any of the following methods can be chosen:-</li>
<ul>
<li>DOUBLE ELIMINATION:
Upon losing two matches, the robot is out of the contest. A good way to run a double-elimination playoff is to have the set of winning robots pitted against other winning robots, and losing robots against losing robots. That way, a robot won't have to compete against two top robots in row. For example, robot A loses to robot B and robot C loses to robot D. Robot A would then play against robot C, with the losing robot being eliminated.</li>
<li>HEATS:
	The robots are divided into smaller groups and a method above is used within each group to organize one-on-one matches. The winners of each group will play in further rounds.</li>
    </ul>
    </li>
    </ul>
<h4>RECORDS, TIE BREAKING, AND CHAMPIONSHIP MATCH:</h4>
<ul>
<li>	Records should be kept to facilitate dispute resolution and tie breaking. The number of match wins is most important, but some good tie breaking criteria are: round losses, round wins, lightest weight, and total time taken per match win. Sudden death rounds could also be used.</li>
<li>	Regardless of the method chosen, organizers should communicate the scoring method well in advance, in writing, so that the participants can design according. An excellent ending to the contest is to extend the championship match to be the first robot to win by two rounds.</li>
</ul>
<h4>A MATCH:</h4>
<ul>
<li>	At the beginning of a match, the contestants approach the ring and bow to each other.</li>
<li>	During the countdown, if the contestant notices their robot has failed to start its countdown, the contestant may alert the referee and halt the countdown. Both robots are reset to start the round over. The contestant is given a warning.</li>
<li>	A second warning of any kind in a single round results in the robot losing that round.</li>
<li>	It is to the benefit of the contestant to stop the countdown if the robot fails to start the first time. It may still be to the benefit of the contestant to stop the countdown a second time (losing the round) if the robot is likely to suffer damage by failure to start.</li>
</ul>
<h4>FALSE START: </h4>
<ul>
<li>	If a robot begins moving during the five-second period, the robot has committed a false start. A warning is issued and both robots are reset to start the round over. A second warning of any kind in a single round results in the robot losing that round.</li>
</ul>
<h4>OUT: </h4>
<ul>
<li>	A robot loses a round when any portion of the robot touches outside of the ring. It doesn't matter if the robot falls out on its own or is pushed out.</li>
<li>	The first robot touching outside of the ring loses, even if the second robot subsequently touches outside of the ring. If the referee determines that both robots touched outside of the ring at the same Time, the round is nullified and started over.</li>
<li>	If any piece of the robot, no matter how small or even if detached, touches outside of the ring, the robot is considered out. For example, if a nut drops off a robot within the ring, the robot doesn't immediately lose. However, if the nut is then pushed out or rolls out, the robot loses.</li>
</ul>
<h4>CONTESTANT STOPPAGE:</h4>
<ul>
<li>	At any time after the five-second starting countdown is over, a contestant may choose to enter the exterior space or otherwise signal stoppage to the referee. That contestant's robot loses the round.</li>
<li>	If the robot is malfunctioning or in a position in which damage could occur, it might be in the contestant's interest to halt the round and take the short-term loss. A contestant that communicates with a robot, attempts to distract (such as with an IR or laser emitter), or in any way attempts to interfere with the outcome is also considered signalling stoppage.</li>
</ul>
<h4>REFEREE STOPPAGE: </h4>
<ul>
<li>	At the referee's discretion, the referee may choose to restart a round if: Three minutes have expired, No progress has been made in some period of time, The robots fail to touch each other for some period of time, the robots are hopelessly entangled or otherwise deadlocked, Both robots fail to start or both contestants signal stoppage. At the referee's discretion, the referee may choose to end a round and choose the round winner if: Smoke, fire, damage, or any other violation has occurred No progress is likely to be made even if the round is restarted. </li>
</ul>
<h4>END OF THE ROUND: </h4>
<ul>
<li>	At the end of a round, the contestants retrieve their robots and prepare them for the next round.</li>
</ul>
<h4>END OF THE MATCH: </h4>
<ul>
<li>	The first robot to win two rounds, wins the match. This means there can be as few as two very quick rounds to win a match. Or there can be as many as three, three-minute rounds (not counting restarts). In the latter case, each robot must have won a round to force the third-round tiebreaker.</li>
<li>	Of course, a match may also end if a contestant or robot is disqualified or otherwise unable to complete.</li>
<li>	At the end of the match, the contestants bow to each other, just as they did at the beginning of the match. They then leave the ring area to prepare their robots for any additional matches in the contest.</li>
</ul>
<h4>HARMLESSNESS:</h4>
<ul>
<li>	At all times, robot behavior must be non-offensive, non-destructive, and non-harmful to humans, robots, and the facilities. This is an immutable principle, even if the behavior is unintentional or not by design.</li>
<li>	During inspection (and at any time during the event), the judges may require safety changes or other modifications to meet the harmlessness requirement. Harmful robots are either not allowed to compete at all or are later disqualified if potential harmful issues are proven or revealed in battle.</li>
<li>	Judges also examine to see if a robot's design is sufficient to survive the expected pushing, shoving, and physical rigors of competition. Suggestions may be made to avoid damage to the robot. A weak robot is usually allowed to compete at its own risk.</li>
<li>	During the contest, the judge must determine if a robot's failure was due to its own lack of durability.</li>
<li>	Failures due to exposed wires or unsecured or flimsy parts shall be the responsibility of the robot with such weaknesses.
	At all times, Sumo Robots must not:
<ul>
<li>	Emit smoke or fire.</li>
<li>	Leak, stain, or soil.</li>
<li>	Disperse powder, grit, or grime.</li>
<li>	Spray, throw, or use projectiles.</li>
<li>	Jam, shock, or electromagnetically interfere.</li>
<li>	Snare, entangle, or employ nets/rope.</li>
<li>	Scratch, gouge, or scrape.</li>
</ul>
</li>
</ul>
However, as for the last point, naturally some damage occurs from scoops, changes in starting orientation, and from the battles themselves. This is expected and acceptable to any reasonable extent.

<h4>THINGS TO BE TAKEN CARE OF:</h4>
Suction, glue, "sticky wheels", magnets, or other methods of increasing downward force will not be allowed. A common method of determining a violation is to place the robot on a piece of paper and lift up the robot to see if the paper lifts too and if this kind of activity is found out then taken of disqualification.
