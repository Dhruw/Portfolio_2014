<h4>RULES AND REGULATIONS:</h4>
<ul>
<li>	3 participants in the team.</li>
<li>	4 teams will start the race at the same time.</li>
<li>	Participants unable to complete the arena due to any technical fault in first round only; can participate again by re-registration. </li>
<li>	The vehicle should be in running condition. (It should run over the arena).</li>
<li>	No one can touch the vehicle in between the arena; else he/she will be subjected to disqualification.</li>
<li>	If the machine is wired then the wire should remain slack under all circumstances during the competition.</li>
<li>	Event is divided in four rounds:</li>
<li>	Winning criteria is to complete the whole arena in minimum time.</li>
<li>	The number of qualifying teams for each round will be declared on the spot.</li>
<li> Only two participants can allow in the field of arena at the time of match.</li>
</ul>

<ul>
<li>ROUND 1:
	<ul>
    <li>	In this round which bot will  complete the arena without hurdle in minimum time will go to next round.</li>
    <li>	Participant will get disqualify if the bot will come across the boundary line.</li>
    </ul>
</li>
<li>ROUND 2:
	<ul>
    <li>	In this round which bot will complete the arena given hurdles in minimum time will go to the final round.</li>
    <li>	Only one more chance will be given to the team if the bot will come across the boundary line otherwise participant will get disqualify.</li>
    </ul>
</li>
<li>ROUND 3:
	<ul>
    <li>	In the final round which bot will complete the arena with hurdles and run over the oil pool in minimum time will be the winner.</li>
    <li>	Only two more chance will be given to the team if the bot will come across the boundary line otherwise participant will get disqualify.</li>
    </ul>
</li>
</ul>

