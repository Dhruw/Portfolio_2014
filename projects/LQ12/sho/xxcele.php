<ul>
<li>	There are several zones where different kinds of Hurdles are placed.</li>
</ul>
<h4>TYPES OF HURDLES:</h4>
<ul>
<li>	Bricks</li>
<li>	Small Wooden Blocks / pebbles</li>
<li>	Oil</li>
<li>	Sand</li>
<li>	Elevation</li>
<li>	Inclination + wooden block</li>
<li>	Mesh (Wire)</li>
<li>	Glass</li>

</ul>
<h4>ROBOT:</h4>
<ul>
<li>	Vehicle should be fit in the box of 250mmX250mmX250mm strictly followed.</li>
<li>	The vehicle should be under the weight of 2 kg.</li>
<li>	Vehicle can be controlled with wired/wireless remote.</li>
<li>	If team uses wired remote then the wire must be minimum of 5 meters.</li>
<li>	Maximum 12Volts DC supply should be used.</li>
</ul>
</div>

