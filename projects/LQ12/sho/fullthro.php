<h4 >Propulsion & Steering</h4>
<ol>
<li>	The machine must use only mechanical power generated by an internal combustion (IC) engine for propulsion. Only one IC engine should be used in the machine. Use of any other sources such as chemicals, compressed gas, rockets etc. is not allowed.</li>
<li>	Any machine which uses DC Motors for propulsion will be disqualified. However DC motors and servos can be used for steering mechanisms or any other control mechanisms apart from propulsion.</li>
<li>	The machine must have an on-board power supply to run any mechanism requiring electric power.</li>
<li>	The maximum allowed capacity of IC engine to be used is 4.6 cc (i.e. Participants can also use 2.5 cc, 3 cc, 3.5 cc or any other IC engine lower in capacity).</li>
<li>	The electric voltage anywhere in the machine should not exceed 12V at any point of time.</li>
<li>	There shall be a countdown preceding the start of the race. No participant is allowed to touch the machine during the countdown period.</li>
<li>	Providing a clutch mechanism between the engine and the wheel would prove useful, as it would prevent the engine from dying out at any stage of the race.</li>
<li>	Participants are advised to use a proper cooling mechanism to prevent overheating of the engine</li>
<li>	Participants are advised to use sway bars for better control and stability.</li>
<li>	The participants are advised to use proper air filters as dirt might cause serious problems to the engine.</li>
<li>	Readymade wheels are allowed.</li>
<li>	The machine will be inspected and if found to be dangerous, the team will be disqualified. This decision rests solely with the judges and the organizers.</li>
</ol>

