<h4>ARENA </h4>
The track will be an all terrain track with sharp turns and big jumps. In this edition of Full Throttle both speed and control will be important. The car should be able to cross bumps, jumps and other obstacles on the track and outdo the opponents' car while maintaining its stability and not compromising on its pace. The track will be an off road track. Most parts of the track will be made up of mud, however at a few places wood and jute mat can be used.
The obstacles in the track are as follows:
<ol>
<li>	One Big bump of 1.5 feet height and width 3 feet.</li>
<li>	Small bumps of 3 inch diameter and of semi-circular shape.</li>
<li>	Small Jump of height 6 inch and length 2.5 feet</li>
<li>	Big Jump of height 1 feet and length 4 feet.</li>
<li>	Bumps in the form of inverted baskets of diameter 16 inch and maximum height of 3 inch.</li>
<li>	Trees of width 1-2 feet may be present obstructing the vision of driver.</li>
<li>	The width of the track will be around 7 feet for most of the part.</li>
<li>	A control stand will be provided along the track for the driver to position himself to control his vehicle during the run. The height of the stand will be around 8 feet. The controlling of machine has to be done from this site only.</li>
<li>	Despite organizers efforts to keep the track quality intact, the track is subjected to undergo wear and tear as machines run over it. The machine is expected to be able to perform on such a weakened track.</li>
<li>	Multiple machines will race on a single lane track at a time.</li>
<li>	A lap of the track is defined to be completed when the machine comes back to the start-finish line.</li>
</ol>

