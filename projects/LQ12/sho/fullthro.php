<H3>PROBLEM STATEMENT</H3>

Make a wireless remote controlled machine, powered by only an IC engine, which can race against other opponents on an off-road dirt track with many obstacles. 

<H4>Machine Specifications</H4>
<ol>
<li>	Machine should fit in a box of dimensions 800 mm x 600 mm x 600 mm (l x b x h) at any moment of time during the race. The external device which is used to control the machine is not included in the size constraint.</li>
<li>	The machine should be controlled by a wireless remote control mechanism throughout the race.</li>
<li>	The machine must not be made from Lego parts, or any ready-made assembly kits other than the parts mentioned below. Readily available chassis layouts are not allowed. Any machine found having a ready-made chassis will be disqualified.</li>
<li>	The machine may be roughly classified into structural and functional parts:</li>
<li>Functional parts - Gears, differential gear, engine, springs, shock absorbers, servo motors (non-propulsion purposes only), batteries, wheels, wheel hub are allowed to be used as available in the market.</li>
<li>	Structural parts - Chassis, steering mechanism, shock towers and suspension (excluding upper suspension arm, suspension spring and shock absorbers) have to be built by the participants themselves.</li>
<li>	Judging for the same will be strict and the participant will be immediately disqualified if any of the above structural components are found to be ready-made.</li>
<li>	The tires used must have a minimum diameter of 3 inches. You are advised to use tyres of good width for better performance on dirt tracks. <br />
