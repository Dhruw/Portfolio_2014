<h4>RULES AND REGULATIONS:</h4>
<ol>
<li>	There will be a qualifying session with each team getting 2 laps out of which the faster lap will be considered. Note that these laps will have to be taken successively.</li>
<li>	The top teams from the qualifying rounds will make it to the second round.</li>
<li>	After the qualifying round there will be races with multiple cars racing at a time. So the participants must use remote with frequency of band spectrum 2.4 GHz.</li>
<li>	The track will have check points at regular intervals. If a machine tumbles, or halts, or goes off the arena at any point on the track, one of the team members is allowed to lift it up and place it at the nearest checkpoint behind that point. The time shall still be running in the meantime.</li>
<li>	Team members are not permitted to touch either their machines or those of their opponents once the race begins (unless there is need to lift the machine as stated in fourth point). The penalty for doing so is disqualification.</li>
<li>	In the qualification round there will be maximum two team members allowed from a team in the racing arena while in the final round only one of the team member will be in the racing arena except the controller on the stand.</li>
<li>	The machines are not allowed to leave any loose parts on any part of the arena. Any machine disintegrating during the race will be disqualified.</li>
<li>	If any of the machines starts off before the flag is waved, the counter would be restarted and the machines will get a second chance. However, if any machine starts off before the waving of flag (or countdown) for a second time, it will be disqualified. No re-match will be held for the second time.</li>
<li>	Teams are not allowed to purposefully damage the machine of the opponent's team. If found doing so on track (while racing), the concerned team will be disqualified. Execution of last three rules will be subjective and relies completely on judges' discretion.</li>
</ol>

<h4>CONTROL RULES:</h4>
<ol>
<li>	Participants are required to use a remote control of frequency of band spectrum 2.4 GHz.</li>
<li>	The above rule is to ensure that there is no frequency clash during the competition and participants are not allowed to use any other remotes with frequency other than 2.4 GHz.</li>
</ol>

<h4>GENERAL RULES:</h4>
<ol>
<li>	The teams may obtain a time slot for practice before first round of the game. The number of practice slots will be limited and the slots will be given on a first come first served basis. The teams will get to use the track only during the time of their practice slot. Any requests for using the track at any other time will not be entertained.</li>
<li>	Any team that is not ready at the time specified will be eliminated from the competition automatically.</li>
<li>	The teams must adhere to the spirit of healthy competition. The teams must not damage the opponent's machine in any way. Judges reserve the right to disqualify any team indulged in misbehaviour.</li>
<li>	The name of your machine should be prominently displayed on the machine.</li>
<li>	Judges decision shall be treated as final and binding on all.</li>
<li>	The organizers reserve the rights to change any or all of the above rules as they deem fit. Change in rules, if any will be highlighted on the website and notified to the registered participants.</li>
</ol>

<h4>JUDGING CRITERION:</h4>
In the qualification round on the basis of individual best times for completing one lap, top teams will be selected who will get into the final round. In the finals multiple teams will race together to triumph over the others.
<h4>TEAM SPECIFICATIONS:</h4>
A team may consist of a maximum of 5 participants. Students from different educational institutes can form a team.

