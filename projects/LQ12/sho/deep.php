<h3>SPECIFICAIONS:</h3>
<h4>ARENA:</h4>
<ul>
<li>	Arena will be of 5m x 3 m.</li>
<li>	Depth of the arena will be 2 feet.</li>
</ul>
<h4>ROBOT:</h4>
<ul>
<li>	The robot should fix in the box of dimensions 400mmX300mmX300mm (LxBxH). </li>
<li>	Robot cannot be expanded and it should remain in its dimension constrain at any time during the game.</li>
<li>	Maximum 12 V DC supply can be used.</li>
<li>	Robot should be controlled manually.</li>
<li>	The robot should have a mechanism of dragging the ball.</li>
<li>	In case if a team is using wireless kit for controlling then the team has to take care of their electronic component such as receiver, and Batteries they must be sealed in a container.</li>
<li>	If the machine is wired control, then the wire of the machine remain slack under all circumstances during the competition.</li>
<li>	Length of wire should be minimum of 6 meter.</li>
<li> Diameter of the ball is  40 mm.</li>
</ul>
</div>

