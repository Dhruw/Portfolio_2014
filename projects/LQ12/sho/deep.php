<h4>RULES AND REGULATIONS:</h4>
<ul>
<li>	There should be 3 participants per team.</li>

<li>	The team has to follow it own lane, if it moves to the other's lane then it consider as the trouble for the other team and it may subjected to Disqualification.</li>
<li>	Team has to take care of their robot components it should be well fastened and fix to its base.</li>
</ul>
<h4>ROUNDS:</h4>
<ul>
<li>	ROUND 1:
    <ul>    <li>	In this round bot simply complete water race in minimum time will go to the next round.</li>    </ul> </li>
<li>	ROUND 2:
	<ul>	<li>Two teams are racing together for the victory poles; the team which crosses the poles first will be the clear winner and move for the next round.</li></ul></li>
<li>	ROUND 3:
	<ul>
    <li>	In this round the bot has to pick/drag the ball placed at the starting line and then the bot will start the race and the team which reaches the finishing line first will be the winner.</li>
   
    </ul>
</li>

<li>	ROUND 4:
	<ul>	<li>In the final round there will 5 -5 balls given in the arena for each team, and each side there is goal post where opponent team has to make a goal over other side. (water socker).</li>
	<li>The team which will put all the balls in the goal post in minimum time will be the winner of the round.</li>
	<li>There will be three games in the final round; the team who wins 2 games with minimum time will be the final winner.</li>
	</ul></li>
</ul>
