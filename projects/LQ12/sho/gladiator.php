<h3>SPECIFICATION:</h3>
<h4>ARENA:</h4>
<ul>
	<li>	Arena will be of square shape of sides 400cm x 400cm.</li>
	<li>	The arena is made up of <i>cement floor</i> and rubber painted surrounded by wire cage.</li>
</ul>

<h4>ROBOT:</h4>
<ul>
	<li>	The machine should fit in a box of dimensions 750mm X 750mm X 1000mm (L x B x H) at every given point of game. The external devices used to control the machine are not included in the size constraint.</li>
	<li>	The machine should not exceed 40kgs of weight.</li>
	<li>	The machine can be controlled wirelessly or with wires. Both <b>ON</b> and <b>OFF</b> board power supplies are allowed.</li>
	<li>	If the machine is wired then the wire should remain slack under all circumstances during the competition.</li>
	<li>	If the machine is controlled wirelessly, the machine must have a dual frequency remote control circuit or two control circuits which may be interchanged before the start of the race to avoid frequency interface with other teams.</li>
	<li>	Remote control systems available in market may be used.</li>
</ul>

<h4>SOURCE OF POWER:</h4>

<ul>
	<li>	The machine can be powered electrically only, use of IC engine in any form is not allowed.</li>
	<li>	Batteries must be sealed, immobilized electrolyte types (such as gel cells, lithium, NiCad, NiMH, or dry cells).</li>
	<li>	The electric voltage anywhere in the machine should not be more then 36V DC at any point of time.</li>
	<li>	All efforts must be made to protect battery terminals from a direct short and causing a battery fire.</li>
</ul>

<h4>PNEUMATICS:</h4>

<ul>
	<li>	Robot can be use pressurized inflammable gases to activate pneumatic devices.</li>
	<li>	Maximum allowed pressure is 8 Bar.</li>
	<li>	Participants must be able to indicate the used pressure with integrated or temporarily fitted pressure gauge.</li>
	<li>	The maximum pressure in cylinder should not exceed the rated pressure at any point of time.</li>
</ul>

<h4>HYDRAULICS</h4>
<ul>
	<li>	Robot can use pressurized liquid to actuate hydraulic devices e.g. cylinders.</li>
	<li>	All hydraulic components onboard a robot must be securely mounted. Particular attention must be made to pump and accumulator mounting and armour to ensure that if ruptured direct fluid streams should not escape.</li>
	<li>	All hydraulic liquids are required to be non corrosive and your device should be leak proof.</li>
	<li>	Maximum allowed pressure is 8 bars.</li>
	<li>	Participant must be able to indicate the used pressure with integrated or temporarily fitted pressure gauge.</li>
</ul>

<h4>WEAPON SYSTEMS:</h4>
<ul>
	<li>	Robots can have any kind of cutters, flippers, saws, lifting devices etc as weapons with following exceptions and limitations:</li>
	<li>	Liquid Projectiles.</li>
	<li>	Any kind of flammable liquids.</li>
	<li>	Flame based weapons.</li>
	<li>	Any kind of explosive or intentionally ignited solid.</li>
	<li>	Nets, tape, glue, or any entanglement device.</li>
	<li>	Radio jamming, tazers, tesla coils, or any other high voltage device.</li>
	<li>	Un-tethered projectiles. </li>
	<li>	Tethered projectiles are allowed with a maximum tether length of 4 feet.</li>
</ul>
