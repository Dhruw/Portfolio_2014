<h4>RULES AND REGULATIONS:</h4>
<ul>
<li>	A team may consist of maximum 6 participants.</li>
<li>	A robot is declared victorious if his opponent is immobilized.</li>
<li>	A competitor will be declared immobile if it cannot display linear motion of at least one inch in a time period of 30 seconds. A robot with one side of its drive train disabled will not be counted out if it can demonstrate some degree of controlled movement.</li>
<li>	In case both the robots remain mobile after the end of the round the winner will be decided subjectively.</li>
<li>	Point will be given on the basis of <b>Aggression and Damage</b>.</li>
<li>	Duration of each match will be 3 minutes.</li>
<li>	Maximum Duration of <b>Semi Final</b> and <b>Final</b> match will be decided at the time of event..</li>
<li>	The machine would be checked for its safety before the game and would be discarded if found unsafe for other participants and spectators.</li>
<li>	All the participants will bring their own robotic kits.</li>

</ul>
<h4>Round 1</h4>
<ul>
<li>This will be a knock out round.</li>
<li>Two teams will play against each other and based upon point scored, they will be qualifying for the next round.</li>
</ul>
<h4>Round 2</h4>
<ul><li>In this round, again a knock out match will be played.</li>
<li>The teams will get qualified for the next round on the basis of following criterion:<br />
a)	If the opponent becomes immobile during match.<br />
b)	In case both of the bot are in mobile condition for the whole match duration, the decision will be taken on the basis of point secured.
</li>
<li>The number of qualifying teams will be decided on the spot</li>
</ul>
<h4>SAFETY RULES:</h4>
<ul>
<li>	All participants build and operate robots at their own risk. Combat robotics is inherently dangerous. There is no amount of regulations that can encompass all the dangers involved. Please take care not to hurt yourself or others when building, testing, and competing.</li>
<li>	If you have a robot or weapon design that does not fit within the set categories of rules or is in some way ambiguous or borderline, please contact the event organizers.</li>
<li>	All weapons must have a safety cover on sharp edges.</li>
</ul>
