<h3>SPECIFICATIONS:</h3>
<h4>ARENA:</h4>
<ul>
<li>	Arena will be of (3000 x 2000)mm</li>
<li>	It has made up of plywood coated with velvet cloth.</li>
<li>	It has two goal post of dimension (400X150X200) mm.</li>
</ul>
<h4>ROBOT:</h4>
<ul>
<li>	Dimensions should not exceed 300mmX250mmX250mm (LxBxH).</li>
<li>	Weight of bots depends on participant's choice.</li>
<li>	The control of bots can be of wired or wireless.</li>
<li>	The power supply should not exceed 12 volts, and should be on board.</li>
<li>	Wire should be slack during the course of match. It should be of minimum 4 meter.</li>
</ul>
<h4>BALL:</h4>
<ul>
<li>	The ball will be standard tennis ball.</li>
</ul>
</div>

