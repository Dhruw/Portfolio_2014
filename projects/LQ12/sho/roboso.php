<h4>RULES AND REGULATIONS: </h4>
<ul>
<li>	There should be 3 participants per team.</li>
<li>	A team shall consist of only one robot.</li>
<li>	Point criterion will be informed on the day of the event, before commencement of the round.</li>
<li>	Robots should shoot the goal before 35 cms. of the goal post(10 cms. tolerance is acceptable</li>
<li>	No readymade kits are allowed.</li>
<li>	Measurement will be done <b>before</b> the start of event by a panel of referee.</li>
<li>	It is the responsibility of the teams to have their robots re-inspected if their Robots are modified at any time during the tournament.</li>
<li>	The robot should not damage the arena by any mean.</li>
<li>	If both robots are immobile for 5 sec, the ball will be replaced at the starting points.</li>
<li>	If three fouls are committed by a team a penalty shootout will be given to the opponent team.</li>
</ul>
<h4>BALL MOVEMENT:</h4>
A robot cannot "hold" a ball (Hint: Holding a ball means taking a full control of the ball by removing all of its degrees of freedom).
<ul>
<li>	The ball cannot be held underneath a robot.</li>
<li>	The ball must be visible at all times.</li>
<li>	Opponent must be able to access the ball.</li>
<li>	All the disqualified participants/teams in the initial rounds will compete for the third position.</li>
<li>	All the participants will bring their own robotic kits.</li>
<li>	Decision of the judges and the event organizers shall be treated as final.</li>
<li>	Own goals will be treated as a goal of the opposition and the point will be given to the opponent team.</li>
<li>	Penalty shoot will be made from 35cms from the goal post.</li>
</ul>
Round 1:
<ul>
<li>	This is knockout round.</li>
<li>	Two team will play against each other.</li>
<li>	The maximum time allotted for the match is 10 minutes (5+5).</li>
<li>	The time break between two halves is 2 minute.</li>
<li>	Time out of 1 min. can be taken by each team during the match for once.</li>
<li>	The winning teams will be selected for next round.</li>
<li>	In case of tie, penalty shootout will be given.</li>
</ul>
Round 2:
<ul>
<li>	This is a league match.</li>
<li>	Match duration and time outs will be as in the previous round.</li>
<li>	Top two team will be selected for final.</li>
</ul>
Round 3:
<ul>
<li>	This is one-on-one round.</li>
<li>	The rules will be informed before the start of match.</li>
</ul>
