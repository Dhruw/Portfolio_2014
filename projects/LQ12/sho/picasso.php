<h4>RULES AND REGULATIONS: </h4>
<ul>
<li>	A team can have max. 3 participants.</li>
<li>	Team which complete, the given design with the minimum time, will become the clear winner.</li>
<li>	In case of many entries for participants the rules are subjected to be change.</li>
<li>	The team can pick as many ply board and place over the robot so that they can complete their task as early as possible.</li>
<li>	If the team has declared that their design is completed then they cannot touch the painting zone unless and until the judging is not done. If they do so then the team is disqualified from the game.</li>
<li>	The team can arrange maximum of 3 cube, at the wrong place and if any more cube is not in its actual place then the team is disqualified from the game.</li>
<li>	Any kind of attachment which harms the arena is not allowed or it may subject to disqualification from the game.</li>
<li>	The gripper of the robot should not have any sticky element like (GUM, TAPES etc) or in short it cannot harm the cube and the drawing/ deign or place any kind of mark on it.</li>
</ul>
