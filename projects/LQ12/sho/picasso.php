<h4>ARENA:</h4>
<ul>
	<li>	Arena will be of 3mX4m.</li>
	<li>	There are three zones in the arena for two consecutive teams.</li>
    <ul>
      <li>	Starting Zone: This is the place where robot is placed for playing the game.</li>
      <li>	Buffer Zone: This zone is having cubes which are arranged randomly on both the sides of arena on step provided on arena.</li>
      <li>	Painting Zone: This is the place where the bot has to arrange the cube design to complete the given drawing/ design by placing them horizontally.</li>
    </ul>
</ul>
<h4>ROBOT:</h4>
<ul>
    <li>	The robot should fit in the box of dimensions 350mmX350mmX500mm (LxBxH).</li>
    <li>	The gripper of the robot should be well equipped to lift the semi metallic cubes.</li>
    <li>	In the game robot attachment can be expanded to any degree of freedom except at the starting of the game.</li>
    <li>	Robot should not damage the arena at any case; otherwise team will be disqualified from the game.</li>
    <li>	Maximum of 12 V DC supply is allowed.</li>
    <li>	Robot can be controlled wired or wirelessly. Both On and Off board power supplies are allowed.</li>
    <li>	If the machine is wired control, then the wire of the machine remain slack under all circumstances during the competition.</li>
    <li>	Length of wire should be minimum of 8 meter.</li>
	<li>	Picking mechanism can be made up of gripper /solenoid type.</li>
</ul>
<h4>CUBES DETAIL:</h4>
<ul>
<li>	Dimension: 10x10x10 cm.</li>
<li>	Weight: 150 gram approx (cube consist of six faces of which one or two faces are used for placement and rest four would have drawing/ design on opposite faces.)</li>
</ul>
</div>

