 <h3>INTRODUCTION:</h3>      
      If you're fond of playing puzzles, solving math problems, and think logic reasoning you can actually combine them all in solving a Sudoku puzzle. Sudoku, commonly known as Number Place, is a logic placement puzzle. The aim of the puzzle is to place the numbers from 1 to 9 in each cell of a 9x9 grid that is made up of 3x3 sub grids or regions. To start playing the game, you are given numbers in some cells referred to as "givens". The goal of the puzzle is to fill in all of the blank cells with the numbers 1-9, with each number appearing only once in each sub grid, row and column.
