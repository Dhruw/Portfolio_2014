<h4>RULES AND REGULATIONS:</h4>
<ul>
	<li>	The event will be held in two rounds.</li>
    <li>	The final testing of the structures will be done on the same day of completion of event..<li>
    <li>	A team may consist of 2 participants.</li>
   
</ul>

<h4>ROUND 1: To built a structure: </h4>
<ul>
	<li>	Participants will have to construct a structure using ice cream sticks.</li>
    <li>	The criteria for qualification will be neatness and time taken.</li>
    <li>	Next, weights will be put on the top of the wall. Maximum weight sustaining wall will be the winner.</li>
    <li>	If weights of two competent matches, then minimum tissue paper used will be considered.</li>
    </ul>
    
<h4>ROUND 2: TO BUILD A BRIDGE:</h4>
<ul>
	<li>	Teams will be provided with a problem statement specifying the details of the bridge.</li>
    <li>	Teams will have to construct a bridge structures based on certain restrictions and parameters given at the time of the competition, within a given time frame.</li>
    <li>	Materials (ice cream sticks, fevicol, thread) will be provided, which will be same for all.</li>
    <li>	All intricate rules and regulations will be revealed on the spot.</li>
    <li>	The decision of the judge is final and binding.</li>
    </ul>
    
<h4>JUDGING PARAMETERS:</h4>
<ul>
<li>	Loads will be placed on the bridge on the bridge in the longitudinal direction. Bridge resisting maximum load will be declared winner.</li>
</ul>
