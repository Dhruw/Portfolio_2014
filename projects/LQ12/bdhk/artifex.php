<h4>RULES AND REGULATIONS:</h4>
<ul>
<li>	The event will be organized in three rounds.</li>
<li>	The points will be allotted for each round out of 10</li>
<li>	The winner will be decided on the basis of sum of points in the three rounds.</li>
</ul>

<ul>
<li>	Round 1 :
<ul>
<li>	Make a LOGO and TAGLINE for the concept "GO GREEN".</li>
<li>	Student will bring their own colors according to their wish with necessary accessories except oil colors.</li>
<li>	Sheet will be 1/4<sup>th</sup> of Full sheet </li>
<li>	TIME LIMIT: 30 mins</li>
</ul>
</li>
<li>	Round 2:
<ul>
<li>	Make a design of T- Shirt for GO GREEN.</li>
<li>	Sheet size: 1/2 of Full sheet</li>
<li>	Time limit for second round is 1 hrs and 30 minutes.</li>
<li>	Sheet will be provided by the institution.</li>
</ul>
</li>
<li>	Round 3:
<ul>
<li>	Create an art from wastage. </li>
<li>	The wastage belongs to engineering branches Mechanical Engg, Electronics & Telecommunication Engg, Civil Engg,Computer Science & Engg. , Electrical Engg</li>
</ul>
</li>
<li>	The winner and runner-up of the event on 3rd day will be declared at the end of the day on basis of best art.
</li>
</ul>
