<h4>RULES AND REGULATIONS:</h4>

<ul>
<li>	Individual participation.</li>
<li>	The team will be formed on the spot.</li>
<li>	The event will be divided in two rounds.</li>
<li>	First Round :
<ul>
<li>	Selling Team will be given a stock of products and price band (of minimum and maximum price of the product) in which they have to sell there products. The job is to fetch highest turnover. The team with highest sales will be the winner.</li>
<li>	The team that fetches the highest sales volume shall be awarded with 100 points and others on pro-rata basis.</li>
<li>	Buying Team shall be given a budget in which they have to do shopping in all the counters and save maximum possible money (They will also get a list mentioning Maximum Price of the products). The team with highest shopping in lowest budget shall be the winner.</li>
<li>	The team with highest shopping in lowest budget shall be awarded with 100 points and others on pro-rata basis.</li>
<li>The team with highest total will qualify for the next round</li>
<li>In second half, the role of teams will be reversed</li>
</ul>
</li>
<li>	Second Round :
<ul>
<li>	Teams will be reshuffled.</li>
<li>	In this round, the whole process will be repeated with a different product.</li>
</ul>
</li>
<li>	Every team has to become both buyer and seller at least once.</li>
<li>	The team with best grand total shall be announced as a winning team.</li>
</ul>
